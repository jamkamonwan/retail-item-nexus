// Get fields by section
export const getFieldsBySection = (section: string): NPDFormField[] => {
  return ALL_NPD_FIELDS.filter(field => field.section === section);
};

// Get fields for specific division and user type
export const getFieldsForContext = (
  division: Division,
  userType: string,
  channel: 'online' | 'offline' | 'both'
): NPDFormField[] => {
  return ALL_NPD_FIELDS.filter(field => {
    const divisionMatch = field.applicableDivisions === 'all' || field.applicableDivisions.includes(division);
    const userMatch = field.assignedTo.includes(userType as any);
    const channelMatch = field.channel === 'both' || field.channel === channel || channel === 'both';
    return divisionMatch && userMatch && channelMatch;
  });
};
